//Crear un programa que muestre los numeros de 5 en 5, en el intervalo de 0 a 50
package clase_de_for_y_while;

public class ejercicio3_for_while {
    
    public static void main(String[] args) {
        
        for (int i = 0; i <= 50; i=i+5) {
            System.out.println("Incremento de 5 hasta 50: " + i);
        }
    }
}
